a = 9
b = 4

# Addition of numbers
add = a + b

# Subtraction of numbers
sub = a - b

# Multiplication of number
mul = a * b

# Division(float) of number
div1 = a / b

# Division(floor) of number
div2 = a // b

# Modulo of both number
mod = a % b

# Power
p = a ** b

# print results
print("Add : ",add)
print("Subtract : ", sub)
print("Multiplication : ",mul)
print("Division : ", div1)
print("Division 2nd (Floor) : ",  div2)
print("Modular : ",mod)
print("Power : ",p)
